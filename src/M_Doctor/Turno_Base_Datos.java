    private void cargarTablaBDTurno() {
        DefaultTableModel tblModelo = (DefaultTableModel) TablaTurnosBD.getModel();

        tblModelo.setNumRows(0);
        List<Atendido> listaturnos = bdturn.Tabla_BDTurno();

        listaturnos.stream().forEach(p -> {
            String[] turnos = {p.getId_turn(), p.getTurno(), p.getCedula(), p.getApellido(), p.getNombre(), p.getCedula_doc(), p.getHorario(), p.getFecha(), p.getAtendido()};
            tblModelo.addRow(turnos);
        });
        // limpiarCampos();
    }

    private void BuscarTablaBDTurno() {
        DefaultTableModel tblModelo = (DefaultTableModel) TablaTurnosBD.getModel();

        tblModelo.setNumRows(0);
        List<Atendido> listaturnos = bdturn.Tabla_BDTurnoBUSCAR(TxtCedPac.getText());

        listaturnos.stream().forEach(p -> {
            String[] turnos = {p.getId_turn(), p.getTurno(), p.getCedula(), p.getApellido(), p.getNombre(), p.getCedula_doc(), p.getHorario(), p.getFecha(), p.getAtendido()};
            tblModelo.addRow(turnos);
        });
        // limpiarCampos();
    }
