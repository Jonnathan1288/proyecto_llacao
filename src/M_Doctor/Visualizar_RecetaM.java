    private void BtnBuscarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarPActionPerformed
        // TODO add your handling code here:
        Date datof = DateBusqueda.getDate();
        if (!(TxtCedulaVR.getText().matches("^\\d{10}$"))) {
            JOptionPane.showMessageDialog(null, "Verifique la cedula.");
        } else {
            if (datof == null) {
                JOptionPane.showMessageDialog(null, "Verifique el ingreso de la fecha.");

            } else {
                Buscar_RecetaHistorial();
            }
        }
    }//GEN-LAST:event_BtnBuscarPActionPerformed

